    <script>
        document.addEventListener('DOMContentLoaded', function() {
            function setupTabs(containerId, tabIds) {
                const container = document.getElementById(containerId);
                const tabButtons = container.querySelectorAll('.tab-button');
                const tabContents = tabIds.map(id => document.getElementById(id));
    
                function showTab(index) {
                    tabContents.forEach(content => content.style.display = 'none');
                    tabContents[index].style.display = 'block';
                }
    
                tabButtons.forEach((button, index) => {
                    button.addEventListener('click', function() {
                        container.querySelector('.tab-button.active').classList.remove('active');
                        button.classList.add('active');
                        showTab(index);
                    });
                });
    
                showTab(0); // Show the first tab content by default
            }
    
            setupTabs('tab1', ['slider1', 'slider2']);
            setupTabs('tab2', ['slider3']);
            setupTabs('tab3', ['slider4', 'slider5', 'slider6', 'slider7']);
            setupTabs('tab4', ['slider8']);
        });
        </script>

        <script>
            document.querySelectorAll('.dropdown').forEach(function(dropdown) {
                dropdown.addEventListener('mouseover', function() {
